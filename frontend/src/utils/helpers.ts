export function getGlucoseLevel(value: number): {
  label: string;
  range: string;
  className: string;
  textClass: string;
  bgClass: string;
} {
  if (value < 54) return {
    label: 'Critical Low',
    range: 'Below 54',
    className: 'bg-red-900 text-white',
    textClass: 'text-glucose-critical-low',
    bgClass: 'bg-glucose-critical-low',
  };
  if (value < 70) return {
    label: 'Low',
    range: '54–69',
    className: 'bg-orange-500 text-white',
    textClass: 'text-glucose-low',
    bgClass: 'bg-glucose-low',
  };
  if (value <= 180) return {
    label: 'In Range',
    range: '70–180',
    className: 'bg-green-500 text-white',
    textClass: 'text-glucose-in-range',
    bgClass: 'bg-glucose-in-range',
  };
  if (value <= 250) return {
    label: 'High',
    range: '181–250',
    className: 'bg-yellow-500 text-white',
    textClass: 'text-glucose-high',
    bgClass: 'bg-glucose-high',
  };
  return {
    label: 'Critical High',
    range: 'Above 250',
    className: 'bg-red-600 text-white',
    textClass: 'text-glucose-critical-high',
    bgClass: 'bg-glucose-critical-high',
  };
}

export function getGMILevel(gmi: number): { label: string; color: string; textColor: string } {
  if (gmi < 5.7) return { label: 'Excellent', color: 'text-green-600', textColor: 'text-green-600' };
  if (gmi < 6.5) return { label: 'Good (Pre-diabetes range)', color: 'text-green-500', textColor: 'text-green-500' };
  if (gmi < 7.0) return { label: 'Good Control', color: 'text-primary-600', textColor: 'text-primary-600' };
  if (gmi < 8.0) return { label: 'Fair — Needs Improvement', color: 'text-yellow-600', textColor: 'text-yellow-600' };
  return { label: 'Poor — Needs Action', color: 'text-red-600', textColor: 'text-red-600' };
}

export function formatDate(dateStr: string): string {
  return new Date(dateStr).toLocaleDateString('en-US', {
    month: 'short', day: 'numeric', year: 'numeric',
  });
}

export function formatDateTime(dateStr: string): string {
  return new Date(dateStr).toLocaleString('en-US', {
    month: 'short', day: 'numeric', hour: '2-digit', minute: '2-digit',
  });
}

export function formatTime(dateStr: string): string {
  return new Date(dateStr).toLocaleTimeString('en-US', {
    hour: '2-digit', minute: '2-digit',
  });
}

