export function getGlucoseRangeClass(value: number): string {
  if (value < 54) return 'glucose-critical-low';
  if (value < 70) return 'glucose-low';
  if (value <= 180) return 'glucose-in-range';
  if (value <= 250) return 'glucose-high';
  return 'glucose-critical-high';
}

